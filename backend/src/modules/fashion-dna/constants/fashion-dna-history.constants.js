import { REFRESH_SOURCES } from './fashion-dna-regeneration.constants';

export const HISTORY_CHANGE_REASONS = {
  PROFILE_UPDATED: 'Profile Updated',
  PURCHASE_ACTIVITY: 'Purchase Activity',
  WISHLIST_ACTIVITY: 'Wishlist Activity',
  BROWSING_ACTIVITY: 'Browsing Activity',
  FACE_ANALYSIS: 'Face Analysis Updated',
  AI_REGENERATION: 'AI Regeneration',
  MANUAL_UPDATE: 'Manual Update',
};

export const HISTORY_CHANGE_SOURCES = {
  MANUAL_UPDATE: 'manual_update',
  AI_REGENERATION: 'ai_regeneration',
  GENERATE: 'generate',
};

const SOURCE_TO_REASON = {
  [REFRESH_SOURCES.PROFILE_UPDATE]: HISTORY_CHANGE_REASONS.PROFILE_UPDATED,
  [REFRESH_SOURCES.BODY_ANALYSIS]: HISTORY_CHANGE_REASONS.PROFILE_UPDATED,
  [REFRESH_SOURCES.FACE_ANALYSIS]: HISTORY_CHANGE_REASONS.FACE_ANALYSIS,
  [REFRESH_SOURCES.WISHLIST_UPDATE]: HISTORY_CHANGE_REASONS.WISHLIST_ACTIVITY,
  [REFRESH_SOURCES.PURCHASE]: HISTORY_CHANGE_REASONS.PURCHASE_ACTIVITY,
  [REFRESH_SOURCES.BROWSING_ACTIVITY]: HISTORY_CHANGE_REASONS.BROWSING_ACTIVITY,
  [REFRESH_SOURCES.PRODUCT_VIEW]: HISTORY_CHANGE_REASONS.BROWSING_ACTIVITY,
  [REFRESH_SOURCES.SEARCH]: HISTORY_CHANGE_REASONS.BROWSING_ACTIVITY,
  [REFRESH_SOURCES.CART_UPDATE]: HISTORY_CHANGE_REASONS.BROWSING_ACTIVITY,
  [REFRESH_SOURCES.CLOSET_UPDATE]: HISTORY_CHANGE_REASONS.PURCHASE_ACTIVITY,
  [REFRESH_SOURCES.TRY_ON]: HISTORY_CHANGE_REASONS.BROWSING_ACTIVITY,
  [REFRESH_SOURCES.SAVED_LOOK]: HISTORY_CHANGE_REASONS.WISHLIST_ACTIVITY,
  [REFRESH_SOURCES.STYLIST]: HISTORY_CHANGE_REASONS.BROWSING_ACTIVITY,
  [HISTORY_CHANGE_SOURCES.MANUAL_UPDATE]: HISTORY_CHANGE_REASONS.MANUAL_UPDATE,
  [HISTORY_CHANGE_SOURCES.AI_REGENERATION]: HISTORY_CHANGE_REASONS.AI_REGENERATION,
  [HISTORY_CHANGE_SOURCES.GENERATE]: HISTORY_CHANGE_REASONS.AI_REGENERATION,
};

export function resolveHistoryReason(changeSource) {
  return (
    SOURCE_TO_REASON[changeSource] || HISTORY_CHANGE_REASONS.AI_REGENERATION
  );
}
